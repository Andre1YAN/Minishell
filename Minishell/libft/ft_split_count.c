#include "libft.h"

int	split_count_solo(char const *s, char c)
{
	int		i;
	char	*a;

	i = 0;
	a = (char *) s;
	while (*a)
	{
		while (*a == c)
			a++;
		if (!(*a))
			break ;
		while (*a != c && *a != 0)
			a++;
		i++;
	}
	return (i);
}
