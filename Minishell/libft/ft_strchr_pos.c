#include "libft.h"

int	ft_strchr_pos(const char *s, int c)
{
	int	i;

	i = 0;
	while (i < (int)ft_strlen(s))
	{
		if (s[i] == (char) c)
			return (i);
		i++;
	}
	return (0);
}
