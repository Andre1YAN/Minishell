#include "libft.h"

int	ft_strrchr_pos(const char *s, int c)
{
	int	i;

	i = ft_strlen(s);
	while (i >= 0)
	{
		if (s[i] == (char) c)
			return (i);
		i--;
	}
	return (0);
}
